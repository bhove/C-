//This program calculates the average class size of several programming courses.
#include <iostream>

using namespace std;

int main()
{
    cout << "Class sizes are " << 14 << ", " << 16 << ", " << 13 << ", and " << 19 << "." << endl;
    cout << endl;
    cout << "The average class size is " << (14+16+13+19)/4.0 << "." << endl;
    return 0;
}
