//This program finds the average of four jump heights.
#include <iostream>

using namespace std;

int main()
{
    cout << "Jumps were lengths " << 3.3 << ", " << 3.5 << ", " << 4.0 << ", and " << 3.0 << " meters." << endl;
    cout << endl;
    cout << "The average of these jumps is " << ((3.3+3.5+4.0+3.0)/4) << " meters." << endl;
    return 0;
}
