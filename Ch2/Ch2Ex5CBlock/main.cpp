//This program calculates the area and perimeter of a 5x3 rectangle
#include <iostream>

using namespace std;

int main()
{
    cout << "Calculate the parameters of a rectangle" << endl;
    cout << endl;
    cout << "Rectangle has length of " << 5 << " units and width of " << 3 << " units" << endl;
    cout << "Rectangle has a perimeter of " << (5*2)+(3*2) << " units" << endl;
    cout << "Rectangle has an area of " << (5*3) << " units squared" << endl;
    return 0;
}
